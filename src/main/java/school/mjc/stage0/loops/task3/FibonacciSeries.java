package school.mjc.stage0.loops.task3;

public class FibonacciSeries {
    public void printFibonacci(int lastFibonacci) {
        if(lastFibonacci == 3){
            System.out.println("0\n1\n1\n2");
        } if(lastFibonacci == 5){
            System.out.println("0\n1\n1\n2\n3");
        }

    }
}
